package com.saukat.vo;

public class CoutryClubVO {

	private String name;
	private String addr;
	private String country;
	private String hpddl;
	
	//setter() and getter()
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getAddr() {
		return addr;
	}
	public void setAddr(String addr) {
		this.addr = addr;
	}
	public String getCountry() {
		return country;
	}
	public void setCountry(String country) {
		this.country = country;
	}
	public String getHpddl() {
		return hpddl;
	}
	public void setHpddl(String hpddl) {
		this.hpddl = hpddl;
	}	
}
