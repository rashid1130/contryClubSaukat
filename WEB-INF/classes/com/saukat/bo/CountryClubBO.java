package com.saukat.bo;

public class CountryClubBO {
	private String name;
	private String addr;
	private String country;
	private int package_price;
	private float package_discount;
	private float total_amount;
	private String holiday_package;
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getAddr() {
		return addr;
	}
	public void setAddr(String addr) {
		this.addr = addr;
	}
	public String getCountry() {
		return country;
	}
	public void setCountry(String country) {
		this.country = country;
	}
	public int getPackage_price() {
		return package_price;
	}
	public void setPackage_price(int package_price) {
		this.package_price = package_price;
	}
	public float getPackage_discount() {
		return package_discount;
	}
	public void setPackage_discount(float package_discount) {
		this.package_discount = package_discount;
	}
	public float getTotal_amount() {
		return total_amount;
	}
	public void setTotal_amount(float total_amount) {
		this.total_amount = total_amount;
	}
	public String getHoliday_package() {
		return holiday_package;
	}
	public void setHoliday_package(String holiday_package) {
		this.holiday_package = holiday_package;
	}
	
   
}
